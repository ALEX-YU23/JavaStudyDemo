package org.jgs.oop.day03;

public class Person {
	
	private int age;
	private double height;
	private char sex;
	
	public int getAge() {
		return age;
	}
	
	public void setAge(int age) {
		if(age>=1&&age<=100) {
			this.age = age;
		}else {
			this.age = 20;
		}
		
	}
	public double getHeight() {
		return height;
	}
	public void setHeight(double height) {
		if(height>=150&&height<=220) {
			this.height = height;
		}else {
			this.height = 170;
		}
	}
	public char getSex() {
		return sex;
	}
	public void setSex(char sex) {
		if(sex=='男'||sex=='女' ) {
			this.sex = sex;
		}else {
			this.sex = '男';
		}
	}
	
	
}
