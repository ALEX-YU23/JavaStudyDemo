package org.jgs.enumdemo.day05;

public class Person {
	
	private Gender sex;

	public Person() {
		
	}

	public Person(Gender sex) {

		this.sex = sex;
	}

	public Gender getSex() {
		return sex;
	}

	public void setSex(Gender sex) {
		this.sex = sex;
	}

	@Override
	public String toString() {
		return "Person [sex=" + sex + "]";
	}

}
