package org.jgs.apidemo.day01;

import org.junit.Test;

/**
 * Math 数学类的使用  该类中封装了很多数学操作的方法
 * 
 * final class Math   不能被继承
 * private Math() {}  私有的构造方法
 * 
 * 1.常量
 * static final double E = 2.7182818284590452354 自然对数
 * static final double PI = 3.14
 * @author mosin
 * @DATE 2021年5月19日
 */

public class MathDemo {
	
	@Test
	public void  test01() { //常用的常量
		//E
		double e = Math.E;
		System.out.println(e);
		//PI
		System.out.println(Math.PI);
		
	}
	
	@Test
	public void  test02() {//数学类中方法的使用
		//求平方根
		double sqrt = Math.sqrt(4);
		System.out.println(sqrt);
		//求次幂  pow(double d1,double d2)  d1 指底数 d2 次幂
		double pow = Math.pow(2, 3);
		System.out.println(pow);
		//求两个数字之间的最大值
		int max = Math.max(12, 10);
		System.out.println(max);
		//求两个数字之间的最小值
		int min = Math.min(12, 10);
		System.out.println(min);
		
		//向上取整  返回大于当前值的最小整数
		double ceil = Math.ceil(1.3);
		System.out.println(ceil);
		//向下取整  返回小于当前值的最大整数
		double floor = Math.floor(1.3);
		System.out.println(floor);
		
		//四舍五入
		long round = Math.round(1.4);
		System.out.println(round);
		
		//生成随机数[0,1)  
		double random = Math.random();
		System.out.println(random);

	}

}
