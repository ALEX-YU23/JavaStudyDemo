package org.jgs.day04;
/*
 
  
 打印如下的图形
    *
   **
  ***
 **** 
 
 */
public class Test02 {

	public static void main(String[] args) {
		//控制打印的行数
		for (int i = 1; i < 5; i++) {
			//打印空格
			for (int j = 1; j <=4-i; j++) {
				System.out.print(" ");
			}
			//打印星号
			for (int k = 1; k<=i; k++) {
				System.out.print("*");
			}
			//换行
			System.out.println();
		}
		
	}

}
