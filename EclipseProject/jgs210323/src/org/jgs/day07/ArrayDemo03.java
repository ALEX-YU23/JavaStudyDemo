package org.jgs.day07;

import java.util.Iterator;

/**
 * 
 *保存一个班级学生的成绩
 * 三个班级学生的成绩？
 * 
 * 二维数组的声明
 * int[][]  scores;
 * scores = new int[3][]
 * 
 * scores = new int[3][2]
 * 
 * 赋值  下标
 * 
 * 使用 下标
 * 
 * 
 * @author mosin
 * @DATE 2021年4月8日
 */
public class ArrayDemo03 {

	public static void main(String[] args) {
		
		//声明数组
		int[][] scores;
		//分配空间
//		scores = new int[2][3];
		scores = new int[2][];
		//赋值
//		scores[0][0] = 10;
//		scores[0][1] = 20;
//		scores[0][2] = 30;
//		//scores[0][3] = 30;//ArrayIndexOutOfBoundsException
//		scores[1][0] = 40;
//		scores[1][1] = 50;
//		scores[1][2] = 60;
		
		int[] score1 = {10,20,30,80};
		int[] score2 = {40,50,60};
		scores[0] = score1;
		scores[1] = score2;
		//遍历数组
		for (int i = 0; i < scores.length; i++) {
			for (int j = 0; j < scores[i].length; j++) {
				System.out.print(scores[i][j]+"\t");
			}
			System.out.println();
		}
		
		
	}

}
